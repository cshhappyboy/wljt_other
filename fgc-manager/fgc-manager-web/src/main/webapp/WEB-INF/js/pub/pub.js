$.MMNumberAdd = function(num,xnum){
	return parseFloat(num) + parseFloat(xnum);
};
$.MMNumberSub = function(num,xnum){
	return parseFloat(num) - parseFloat(xnum);
};
$.MMNumberDiv=function(num,xnum){
	return parseFloat(num)/parseFloat(xnum);
};
$.MMNumberMul=function(num,xnum){
	return parseFloat(num)*parseFloat(xnum);
}